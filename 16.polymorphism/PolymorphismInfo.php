<!-- To begin with, Polymorphism is gotten from the Greek words Poly (which means many) and morphism (which means forms).

Polymorphism is just a concept in PHP OOP.

Polymorphism is when we have the same methods of an interface implemented differently in two or more classes that implement the interface.

Polymorphism is also when the same methods of an abstract class are implemented differently in two or more classes that extend it.

So we have polymorphism when we have many forms of the same method. -->